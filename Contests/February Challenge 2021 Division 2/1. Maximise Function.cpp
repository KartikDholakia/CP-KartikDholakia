void solve() {
	ll n, i, inp;
	cin >> n;
	ll mx = INT_MIN, mn = INT_MAX;

	for (i = 0; i < n; i++) {
		cin >> inp;
		if (inp < mn)
			mn = inp;
		if (inp > mx)
			mx = inp;
	}

	cout << (ll)(2*(mx - mn)) << "\n";
}

